def calcular_area_cuadrado (lado):
	area=lado*lado
	return area

def calcular_volumen_paralelepipedo(base,altura, alto):
	volumen=base*altura*alto
	return volumen

def calcular_volumen_cubo(lado):
	volumen=lado**3
	return volumen
	
